#!/usr/bin/env python3

from __future__ import annotations

import argparse
import html
import os
import shlex
import sys
from dataclasses import dataclass, field
from pathlib import Path

try:
    import tomllib
except ModuleNotFoundError:  # pragma: no cover
    import tomli as tomllib


LABWC_BEGIN_MARKER = "<!-- uconsole-helper-mapper:keybinds begin -->"
LABWC_END_MARKER = "<!-- uconsole-helper-mapper:keybinds end -->"
SESSION_LAUNCHER = "/usr/local/bin/uconsole-launch-in-session"
RIGHTSHIFT_LAYER = "uconsole_rightshift"
RIGHTSHIFT_PASSTHROUGH_MODIFIERS = {
    "control": "C",
    "alt": "A",
    "meta": "M",
    "altgr": "G",
}


@dataclass(slots=True)
class RightShiftBinding:
    key: str
    command: str


@dataclass(slots=True)
class LabwcBinding:
    key: str
    command: str | None = None
    action: str | None = None
    attributes: dict[str, str] = field(default_factory=dict)
    comment: str | None = None


def parse_args() -> argparse.Namespace:
    script_dir = Path(__file__).resolve().parent
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--config",
        type=Path,
        default=Path("~/.config/uconsole-helper-mapper/desktop-keybinds.toml").expanduser(),
    )
    parser.add_argument(
        "--keyd-out",
        type=Path,
        default=script_dir / "keyd-uconsole-helper-mapper",
    )
    parser.add_argument(
        "--labwc-out",
        type=Path,
        default=script_dir / "labwc-keybinds.xml",
    )
    return parser.parse_args()


def load_config(path: Path) -> tuple[list[RightShiftBinding], list[LabwcBinding]]:
    raw = tomllib.loads(path.read_text(encoding="utf-8"))
    rightshift = raw.get("rightshift", {})
    labwc = raw.get("labwc", {})

    rightshift_raw = rightshift.get("bindings", [])
    labwc_raw = labwc.get("bindings", [])

    rightshift_keys: set[str] = set()
    rightshift_bindings: list[RightShiftBinding] = []
    for index, item in enumerate(rightshift_raw):
        if not isinstance(item, dict):
            raise ValueError(f"rightshift.bindings[{index}] must be a table")
        key = str(item.get("key", "")).strip().lower()
        command = str(item.get("command", "")).strip()
        if not key or not command:
            raise ValueError(f"rightshift.bindings[{index}] requires key and command")
        if key in rightshift_keys:
            raise ValueError(f"duplicate RightShift binding for key {key}")
        rightshift_keys.add(key)
        rightshift_bindings.append(RightShiftBinding(key=key, command=command))

    labwc_keys: set[str] = set()
    labwc_bindings: list[LabwcBinding] = []
    for index, item in enumerate(labwc_raw):
        if not isinstance(item, dict):
            raise ValueError(f"labwc.bindings[{index}] must be a table")
        key = str(item.get("key", "")).strip()
        command_raw = item.get("command")
        action_raw = item.get("action")
        comment_raw = item.get("comment")
        command = str(command_raw).strip() if command_raw is not None else None
        action = str(action_raw).strip() if action_raw is not None else None
        comment = str(comment_raw).strip() if comment_raw is not None else None
        if not key:
            raise ValueError(f"labwc.bindings[{index}] requires key")
        if bool(command) == bool(action):
            raise ValueError(
                f"labwc.bindings[{index}] requires exactly one of command or action"
            )
        if key in labwc_keys:
            raise ValueError(f"duplicate labwc binding for key {key}")
        labwc_keys.add(key)
        attributes = {
            str(attr_key): str(attr_value).strip()
            for attr_key, attr_value in item.items()
            if attr_key not in {"key", "command", "action", "comment"}
        }
        labwc_bindings.append(
            LabwcBinding(
                key=key,
                command=command,
                action=action,
                attributes=attributes,
                comment=comment or None,
            )
        )

    return rightshift_bindings, labwc_bindings


def render_keyd(bindings: list[RightShiftBinding]) -> str:
    lines = [
        "# Auto-generated by generate_desktop_keybinds.py",
        "# RightShift stays a normal Shift modifier for unmapped combos.",
        "# Listed RightShift combos launch commands directly from keyd.",
    ]
    if not bindings:
        lines.append("# No RightShift desktop bindings configured.")
        return "\n".join(lines) + "\n"

    lines.extend(
        [
            "[main]",
            f"rightshift = layer({RIGHTSHIFT_LAYER})",
            "",
            f"[{RIGHTSHIFT_LAYER}:S]",
        ]
    )
    for binding in bindings:
        launched = shlex.quote(expand_command_home(binding.command))
        lines.append(f"# RightShift+{binding.key.upper()} -> {binding.command}")
        lines.append(f"{binding.key} = command({SESSION_LAUNCHER} {launched})")
    lines.extend(render_keyd_modifier_passthroughs(bindings))
    return "\n".join(lines) + "\n"


def render_keyd_modifier_passthroughs(bindings: list[RightShiftBinding]) -> list[str]:
    lines: list[str] = []
    modifier_items = list(RIGHTSHIFT_PASSTHROUGH_MODIFIERS.items())
    for mask in range(1, 1 << len(modifier_items)):
        active = [
            modifier_items[index]
            for index in range(len(modifier_items))
            if mask & (1 << index)
        ]
        layer_names = "+".join([RIGHTSHIFT_LAYER, *(name for name, _ in active)])
        macro_modifiers = "-".join([*(modifier for _, modifier in active), "S"])
        lines.extend(["", f"[{layer_names}]", f"# Preserve {macro_modifiers} on configured RightShift launcher keys."])
        for binding in bindings:
            lines.append(f"{binding.key} = {macro_modifiers}-{binding.key}")
    return lines


def render_labwc(bindings: list[LabwcBinding]) -> str:
    lines = [LABWC_BEGIN_MARKER]
    for binding in bindings:
        if binding.comment:
            lines.append(f"<!-- {binding.comment} -->")
        lines.append(f'<keybind key="{binding.key}">')
        if binding.command:
            command = html.escape(expand_command_home(binding.command), quote=True)
            lines.append(f'  <action name="Execute" command="{command}" />')
        else:
            action_name = html.escape(binding.action or "", quote=True)
            attrs = "".join(
                f' {html.escape(name, quote=True)}="{html.escape(value, quote=True)}"'
                for name, value in sorted(binding.attributes.items())
            )
            lines.append(f'  <action name="{action_name}"{attrs} />')
        lines.append("</keybind>")
    lines.append(LABWC_END_MARKER)
    return "\n".join(lines) + "\n"


def expand_command_home(command: str) -> str:
    try:
        parts = shlex.split(command, posix=True)
    except ValueError:
        return command

    if not parts:
        return command

    if parts[0].startswith("~/"):
        parts[0] = os.path.expanduser(parts[0])
        return shlex.join(parts)

    return command


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def main() -> int:
    args = parse_args()
    config_path = args.config.expanduser()
    if not config_path.exists():
        print(f"config not found: {config_path}", file=sys.stderr)
        return 1

    rightshift_bindings, labwc_bindings = load_config(config_path)
    write_text(args.keyd_out, render_keyd(rightshift_bindings))
    write_text(args.labwc_out, render_labwc(labwc_bindings))

    print(
        f"generated {args.keyd_out} and {args.labwc_out} from {config_path} "
        f"({len(rightshift_bindings)} RightShift bindings, {len(labwc_bindings)} labwc bindings)"
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
